public class IfStatement {

	/*
	 * if, ifelse, ifelse ladder, nested if
	 */	
	
	public static void main(String[] args) {
			  int a=10;
			  int b=10;
			  if(a!=b) {
				  System.out.println(a+" != "+b);
			  }
			  if(a==b) {
				  System.out.println(a+" == "+b);
			  }
	}

}
