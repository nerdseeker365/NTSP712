package com.nt.litr;

public class LiteralTest {
//Any value that we store into a variable will be called as Literal...
	public static void main(String[] args) {
	//literals in java
		int a=2;
		long l=23L;
		float f=34.00f;
		double d=23.00D;
		char c='a';
		boolean b=true;
		System.out.println(a+"\t"+l+"\t"+f+"\t"+d+"\t"+c+"\t"+b);

	}

}
